package br.org.funcate.glue.model.xml;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlElement;
import javax.xml.bind.annotation.XmlRootElement;
import javax.xml.bind.annotation.XmlType;

/**
 * 
 * \brief This class represents the DCPType node of the XML.
 * 
 * @author Emerson Leite de Moraes
 * @author Willyan Aleksander
 * 
 *         Version 1.0
 */

@XmlAccessorType(XmlAccessType.FIELD)
@XmlType(name = "", propOrder = { "_http" })
@XmlRootElement(name = "DCPType")
public class DCPType {

	@XmlElement(name = "HTTP", required = true)
	protected HTTP _http;

	/**
	 * \brief Gets the value of the http property.
	 * 
	 * @return possible object is {@link HTTP }
	 * 
	 */
	public HTTP getHTTP() {
		return _http;
	}

	/**
	 * \brief Sets the value of the http property.
	 * 
	 * @param value
	 *            allowed object is {@link HTTP }
	 * 
	 */
	public void setHTTP(HTTP value) {
		this._http = value;
	}

}
